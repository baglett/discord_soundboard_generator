class EmojiPicker:
    """
    A searchable emoji picker dialog
    """

    def __init__(self, parent: tk.Widget, callback: Optional[Callable[[str], None]] = None):
        """
        Initialize the emoji picker

        Args:
            parent: Parent widget
            callback: Optional callback function to call with selected emoji
        """
        self.parent = parent
        self.callback = callback
        self.selected_emoji = None

        # Create dialog
        self.dialog = tk.Toplevel(parent)
        self.dialog.title("Select Emoji")
        self.dialog.geometry("600x500")
        self.dialog.minsize(500, 400)

        # Make it modal
        self.dialog.transient(parent)
        self.dialog.grab_set()

        # Build UI
        self._build_ui()

        # Center the dialog
        self._center_dialog()

        # Focus on search box
        self.search_entry.focus_set()

    def _build_ui(self):
        """Build the emoji picker UI"""
        # Main container
        main_frame = ttk.Frame(self.dialog, padding=10)
        main_frame.pack(fill=tk.BOTH, expand=True)

        # Header
        header_frame = ttk.Frame(main_frame)
        header_frame.pack(fill=tk.X, pady=(0, 10))

        ttk.Label(header_frame, text="Select an Emoji", font=('Arial', 12, 'bold')).pack(side=tk.LEFT)

        # Search bar
        search_frame = ttk.Frame(main_frame)
        search_frame.pack(fill=tk.X, pady=(0, 10))

        ttk.Label(search_frame, text="Search:").pack(side=tk.LEFT, padx=(0, 5))
        self.search_var = tk.StringVar()
        self.search_var.trace('w', lambda *args: self._filter_emojis())
        self.search_entry = ttk.Entry(search_frame, textvariable=self.search_var, width=40)
        self.search_entry.pack(side=tk.LEFT, fill=tk.X, expand=True, padx=(0, 5))

        clear_btn = ttk.Button(search_frame, text="âœ–", width=3, command=self._clear_search)
        clear_btn.pack(side=tk.LEFT)

        # Category tabs
        self.notebook = ttk.Notebook(main_frame)
        self.notebook.pack(fill=tk.BOTH, expand=True, pady=(0, 10))

        # Create category tabs
        self.category_frames = {}
        for category_name, emojis in DISCORD_EMOJIS.items():
            tab = ttk.Frame(self.notebook)
            self.notebook.add(tab, text=category_name)
            self.category_frames[category_name] = self._create_emoji_grid(tab, emojis)

        # Button frame
        button_frame = ttk.Frame(main_frame)
        button_frame.pack(fill=tk.X)

        ttk.Button(button_frame, text="Clear Selection", command=self._clear_selection).pack(side=tk.LEFT, padx=(0, 5))
        ttk.Button(button_frame, text="Cancel", command=self._cancel).pack(side=tk.RIGHT, padx=(5, 0))
        ttk.Button(button_frame, text="OK", command=self._ok).pack(side=tk.RIGHT)

    def _create_emoji_grid(self, parent: ttk.Frame, emojis: list) -> tk.Frame:
        """
        Create a scrollable grid of emoji buttons

        Args:
            parent: Parent frame
            emojis: List of emoji characters

        Returns:
            Frame containing the emoji grid
        """
        # Container with scrollbar
        container = ttk.Frame(parent)
        container.pack(fill=tk.BOTH, expand=True)

        # Canvas for scrolling
        canvas = tk.Canvas(container, highlightthickness=0)
        scrollbar = ttk.Scrollbar(container, orient="vertical", command=canvas.yview)
        scrollable_frame = ttk.Frame(canvas)

        scrollable_frame.bind(
            "<Configure>",
            lambda e: canvas.configure(scrollregion=canvas.bbox("all"))
        )

        canvas.create_window((0, 0), window=scrollable_frame, anchor="nw")
        canvas.configure(yscrollcommand=scrollbar.set)

        # Create emoji buttons in grid
        emojis_per_row = 12
        for i, emoji in enumerate(emojis):
            row = i // emojis_per_row
            col = i % emojis_per_row

            btn = tk.Button(
                scrollable_frame,
                text=emoji,
                font=('Segoe UI Emoji', 20),
                width=2,
                height=1,
                relief=tk.FLAT,
                cursor="hand2",
                command=lambda e=emoji: self._select_emoji(e)
            )
            btn.grid(row=row, column=col, padx=2, pady=2)

            # Hover effect
            btn.bind("<Enter>", lambda e, b=btn: b.configure(relief=tk.RAISED))
            btn.bind("<Leave>", lambda e, b=btn: b.configure(relief=tk.FLAT))

        canvas.pack(side=tk.LEFT, fill=tk.BOTH, expand=True)
        scrollbar.pack(side=tk.RIGHT, fill=tk.Y)

        # Store canvas and frame for filtering
        container.canvas = canvas
        container.scrollable_frame = scrollable_frame

        return container

    def _select_emoji(self, emoji: str):
        """Select an emoji"""
        self.selected_emoji = emoji
        self._ok()

    def _filter_emojis(self):
        """Filter emojis based on search term"""
        search_term = self.search_var.get().lower().strip()

        if not search_term:
            # Show all categories
            for category_name in DISCORD_EMOJIS.keys():
                # Re-enable all tabs
                tab_index = list(DISCORD_EMOJIS.keys()).index(category_name)
                self.notebook.tab(tab_index, state="normal")
            return

        # Search through all emojis and find matches
        # For simplicity, we'll search by category name
        matching_categories = []
        for category_name in DISCORD_EMOJIS.keys():
            if search_term in category_name.lower():
                matching_categories.append(category_name)

        # Show/hide tabs based on search
        for i, category_name in enumerate(DISCORD_EMOJIS.keys()):
            if matching_categories and category_name not in matching_categories:
                self.notebook.tab(i, state="disabled")
            else:
                self.notebook.tab(i, state="normal")

        # Switch to first matching tab
        if matching_categories:
            first_match_index = list(DISCORD_EMOJIS.keys()).index(matching_categories[0])
            self.notebook.select(first_match_index)

    def _clear_search(self):
        """Clear the search box"""
        self.search_var.set("")
        self.search_entry.focus_set()

    def _clear_selection(self):
        """Clear the selected emoji"""
        self.selected_emoji = None
        self._ok()

    def _ok(self):
        """Confirm selection and close"""
        if self.callback and self.selected_emoji is not None:
            self.callback(self.selected_emoji)
        self.dialog.destroy()

    def _cancel(self):
        """Cancel selection and close"""
        self.selected_emoji = None
        self.dialog.destroy()

    def _center_dialog(self):
        """Center the dialog on screen"""
        self.dialog.update_idletasks()
        x = (self.dialog.winfo_screenwidth() // 2) - (self.dialog.winfo_width() // 2)
        y = (self.dialog.winfo_screenheight() // 2) - (self.dialog.winfo_height() // 2)
        self.dialog.geometry(f'+{x}+{y}')

    def show(self) -> Optional[str]:
        """
        Show the emoji picker dialog and wait for selection

        Returns:
            Selected emoji or None if cancelled
        """
        self.dialog.wait_window()
        return self.selected_emoji


def show_emoji_picker(parent: tk.Widget, callback: Optional[Callable[[str], None]] = None) -> Optional[str]:
    """
    Show the emoji picker dialog

    Args:
        parent: Parent widget
        callback: Optional callback function to call with selected emoji

    Returns:
        Selected emoji or None if cancelled
    """
    picker = EmojiPicker(parent, callback)
    return picker.show()


